"""
Attack Plan tab for CTF Hunter.

Displays hypotheses generated by the HypothesisEngine, ordered by confidence.
Each hypothesis is expandable to show reasoning and has a copy-to-clipboard command.
"""
from __future__ import annotations

from typing import List

from PyQt6.QtCore import Qt, pyqtSignal
from PyQt6.QtGui import QColor, QFont
from PyQt6.QtWidgets import (
    QApplication,
    QHBoxLayout,
    QLabel,
    QPushButton,
    QScrollArea,
    QSizePolicy,
    QTextEdit,
    QTreeWidget,
    QTreeWidgetItem,
    QVBoxLayout,
    QWidget,
)

from core.hypothesis_engine import Hypothesis, HypothesisEngine
from core.report import Finding, Session


class AttackPlanTab(QWidget):
    """Tab that shows rule-based and AI-generated attack hypotheses."""

    def __init__(self, ai_client=None, parent=None) -> None:
        super().__init__(parent)
        self._ai_client = ai_client
        self._engine = HypothesisEngine(ai_client=ai_client)
        self._hypotheses: List[Hypothesis] = []

        layout = QVBoxLayout(self)
        layout.setContentsMargins(4, 4, 4, 4)

        header = QHBoxLayout()
        header.addWidget(QLabel("Attack Hypotheses (ordered by confidence):"))
        refresh_btn = QPushButton("🔄 Refresh")
        refresh_btn.setToolTip("Re-generate hypotheses from current findings")
        refresh_btn.clicked.connect(self._refresh)
        header.addWidget(refresh_btn)
        layout.addLayout(header)

        self._tree = QTreeWidget()
        self._tree.setHeaderLabels(["Hypothesis", "Category", "Confidence", "Source"])
        self._tree.setColumnWidth(0, 380)
        self._tree.setColumnWidth(1, 120)
        self._tree.setColumnWidth(2, 90)
        self._tree.setColumnWidth(3, 60)
        self._tree.setAlternatingRowColors(True)
        self._tree.itemDoubleClicked.connect(self._on_item_double_clicked)
        layout.addWidget(self._tree, 1)

        # Detail pane
        layout.addWidget(QLabel("Details (double-click a hypothesis):"))
        self._detail = QTextEdit()
        self._detail.setReadOnly(True)
        self._detail.setMaximumHeight(180)
        self._detail.setFont(QFont("Courier", 9))
        layout.addWidget(self._detail)

        copy_btn = QPushButton("📋 Copy Command")
        copy_btn.clicked.connect(self._copy_command)
        layout.addWidget(copy_btn)

        self._session: Session = Session()

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def set_ai_client(self, ai_client) -> None:
        self._ai_client = ai_client
        self._engine = HypothesisEngine(ai_client=ai_client)

    def update_session(self, session: Session) -> None:
        self._session = session
        self._refresh()

    # ------------------------------------------------------------------

    def _refresh(self) -> None:
        self._hypotheses = self._engine.generate(self._session)
        self._populate_tree()

    def _populate_tree(self) -> None:
        self._tree.clear()
        for hyp in self._hypotheses:
            pct = f"{hyp.confidence * 100:.0f}%"
            source_icon = "🤖" if hyp.source == "ai" else "📐"
            item = QTreeWidgetItem([
                hyp.title,
                hyp.category,
                pct,
                source_icon,
            ])
            item.setData(0, Qt.ItemDataRole.UserRole, hyp)
            # Color code by confidence
            if hyp.confidence >= 0.85:
                for col in range(4):
                    item.setForeground(col, QColor("#cc3300"))
            elif hyp.confidence >= 0.65:
                for col in range(4):
                    item.setForeground(col, QColor("#cc8800"))
            self._tree.addTopLevelItem(item)

    def _on_item_double_clicked(self, item: QTreeWidgetItem, _col: int) -> None:
        hyp: Hypothesis = item.data(0, Qt.ItemDataRole.UserRole)
        if hyp is None:
            return
        text = (
            f"Title:    {hyp.title}\n"
            f"Category: {hyp.category}\n"
            f"Confidence: {hyp.confidence * 100:.0f}%\n"
            f"Source:   {hyp.source}\n\n"
            f"Reasoning:\n{hyp.reasoning}\n\n"
            f"Suggested command:\n{hyp.suggested_command}\n"
        )
        if hyp.missing_findings:
            text += f"\nMissing evidence needed:\n" + "\n".join(f"  • {m}" for m in hyp.missing_findings)
        self._detail.setPlainText(text)

    def _copy_command(self) -> None:
        item = self._tree.currentItem()
        if item is None:
            return
        hyp: Hypothesis = item.data(0, Qt.ItemDataRole.UserRole)
        if hyp is None:
            return
        QApplication.clipboard().setText(hyp.suggested_command)
